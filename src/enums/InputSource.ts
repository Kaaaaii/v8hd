/**
 * Enum representing available input sources on Roland V-8HD
 */
export enum InputSource {
    /** HDMI Input 1 */
    HDMI1 = 0x00,
    /** HDMI Input 2 */
    HDMI2 = 0x01,
    /** HDMI Input 3 */
    HDMI3 = 0x02,
    /** HDMI Input 4 */
    HDMI4 = 0x03,
    /** HDMI Input 5 */
    HDMI5 = 0x04,
    /** HDMI Input 6 */
    HDMI6 = 0x05,
    /** HDMI Input 7 */
    HDMI7 = 0x06,
    /** HDMI Input 8 */
    HDMI8 = 0x07,
    /** Still Image 1 */
    STILL1 = 0x08,
    /** Still Image 2 */
    STILL2 = 0x09,
    /** Still Image 3 */
    STILL3 = 0x0A,
    /** Still Image 4 */
    STILL4 = 0x0B,
    /** Still Image 5 */
    STILL5 = 0x0C,
    /** Still Image 6 */
    STILL6 = 0x0D,
    /** Still Image 7 */
    STILL7 = 0x0E,
    /** Still Image 8 */
    STILL8 = 0x0F,
}
